    public void llenarTabla(){
        DefaultTableModel modeltabla = new DefaultTableModel(new String[]{"ID","NOMBRE","Apellido","Departamento"},this.empleados.size());
        this.TablaEmpleados.setModel(modeltabla);
        TableModel DatosModelo= TablaEmpleados.getModel();
        for (int i = 0; i < this.empleados.size(); i++) {
            Empleado p= this.empleados.get(i);
            DatosModelo.setValueAt(p.getId(),i, 0);
            DatosModelo.setValueAt(p.getNombre(),i, 1);
            DatosModelo.setValueAt(p.getApellido(),i, 2);
            DatosModelo.setValueAt(p.getDepartamento(),i, 3);
        }
    }

