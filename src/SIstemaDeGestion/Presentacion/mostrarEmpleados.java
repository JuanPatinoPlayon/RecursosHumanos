    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila=this.TablaEmpleados.getSelectedRow();
        if (fila>=0) {
            Empleado P= this.empleados.get(fila);
            int opcionEL=JOptionPane.showConfirmDialog(this,"¿seguro desea eliminiar el : "+ P.getNombre());
            if (opcionEL==0) {
                this.sistema.eliminarEmpleado(P.getId());
                this.empleados=this.sistema.obtenerEmpleados();
                this.llenarTabla();   
            }
        }else{
            JOptionPane.showMessageDialog(this, "No a seleccionado un producto a eliminar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnActualizarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEmpleadoActionPerformed
        int fila=this.TablaEmpleados.getSelectedRow();
        if (fila>=0) {
            Empleado p= this.empleados.get(fila);
            actualizarEmpleado ventaActualizar= new actualizarEmpleado(this.ventanaPrincipal,true,sistema,p,this);
            ventaActualizar.setVisible(true);
            this.empleados=this.sistema.obtenerEmpleados();
            this.llenarTabla(); 
        }
    }//GEN-LAST:event_btnActualizarEmpleadoActionPerformed

