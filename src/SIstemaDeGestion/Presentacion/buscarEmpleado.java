    private void btnBuscarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEmpleadoActionPerformed
       String nombreBuscar=this.txtNombreEmpleadoBuscar.getText().trim();
       Empleado p=this.sistema.buscarEmpleado(nombreBuscar);
       if (p== null){
           JOptionPane.showMessageDialog(this,"el empleado "+ nombreBuscar + "No se encontro");
       }else{
           JOptionPane.showMessageDialog(this, "El Empleado fue encontrado: \n"
        + "Empleado  \n"
        + "ID:" + p.getId() + "\n"
        +"Nombre: " + p.getNombre() +"\n"
        +"Apellido: "+ p.getApellido() +"\n"
        + "Departamento: " + p.getDepartamento());
       }
    }//GEN-LAST:event_btnBuscarEmpleadoActionPerformed


